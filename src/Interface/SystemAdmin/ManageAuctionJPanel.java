    private void viewDetailsJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailsJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = auctionJTable.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row", "Invalid Option", JOptionPane.WARNING_MESSAGE);
            return;
        }
        BiddingWorkRequest biddingWorkRequest = (BiddingWorkRequest) auctionJTable.getValueAt(selectedRow, 0);
        adJTextField.setText(String.valueOf(biddingWorkRequest.getAd()));
        advertiserJTextField.setText(biddingWorkRequest.getSender());
        currentBidJTextField.setText(String.valueOf(biddingWorkRequest.getBidValue()));
        categoryJTextField.setText(String.valueOf(biddingWorkRequest.getProfileCategory()));
    }//GEN-LAST:event_viewDetailsJButtonActionPerformed


