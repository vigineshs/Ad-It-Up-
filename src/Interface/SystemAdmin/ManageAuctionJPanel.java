    private void populateTopBids() {
        DefaultTableModel tableModel = (DefaultTableModel) auctionJTable.getModel();
        tableModel.setRowCount(0);
        for(BiddingWorkRequest workRequest : enterprise.getAdminOrganization().getWorkQueue().getActiveBids()) {
            Object[] row = new Object[4];
            row[0] = workRequest;
            row[1] = workRequest.getProfileCategory();
            row[2] = workRequest.getAd();
            row[3] = workRequest.getBidValue();
            tableModel.addRow(row);
        }
    }
