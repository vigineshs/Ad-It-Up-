    private void populateAdsTable() {
        DefaultTableModel model = (DefaultTableModel) adsJTable.getModel();
        model.setRowCount(0);
        for(Ads ads : adDirectory.getAdList()) {
            Object[] row = new Object[3];
            row[0] = ads;
            row[1] = ads.getProduct().getProductDescription();
            row[2] = ads.getProduct().getProductCategory();
            model.addRow(row);
        }
    }

