    private void viewAndUpdateJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewAndUpdateJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = adsJTable.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row", "Invalid Option", JOptionPane.WARNING_MESSAGE);
        }
        int option = JOptionPane.showConfirmDialog(null, "Are you sure", "Delete Product", JOptionPane.YES_NO_OPTION);
        if(option == JOptionPane.NO_OPTION){
            return;
        }
        Ads ads = (Ads) adsJTable.getValueAt(selectedRow, 0);
        adDirectory.deleteAd(ads);
        populateAdsTable();
    }//GEN-LAST:event_viewAndUpdateJButtonActionPerformed


