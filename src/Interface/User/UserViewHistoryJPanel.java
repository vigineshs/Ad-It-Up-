    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) historyJTable.getModel();
        model.setRowCount(0);
        for(Order order : userOrganization.getMasterOrderCatalog().getMasterOrderList()) {
            for(OrderItem orderItem : order.getOrders()) {
                if(orderItem.getBuyer().equals(userAccount.getUsername())) {
                    Object[] row = new Object[2];
                    row[0] = orderItem;
                    row[1] = orderItem.getTimeStamp();
                    model.addRow(row);
                }
            }
        }
    }
    
