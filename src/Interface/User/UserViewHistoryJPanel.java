    private void viewDetailsJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailsJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = historyJTable.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row", "Invalid Option", JOptionPane.WARNING_MESSAGE);
            return;
        }
        OrderItem orderItem = (OrderItem) historyJTable.getValueAt(selectedRow, 0);
        productJTextField.setText(orderItem.getProduct().getProductName());
        quantityJTextField.setText(String.valueOf(orderItem.getQuantity()));
        amountJTextField.setText(String.valueOf(orderItem.getAmount()));
    }//GEN-LAST:event_viewDetailsJButtonActionPerformed


